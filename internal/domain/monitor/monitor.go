// Package monitor 提供系统监控功能
//
// 本包实现了各种系统监控器，包括键盘监控等，通过统一的接口管理监控器的生命周期。
// 监控器采用分层架构设计：
//   - 业务层（本包）：负责事件处理、业务逻辑和状态管理
//   - 平台层（platform包）：负责与操作系统交互，捕获底层事件
//
// 主要组件：
//   - Engine: 监控引擎，统一管理所有监控器
//   - KeyboardMonitor: 键盘监控器，捕获键盘输入事件
//
// 事件流程：
//   1. 平台层捕获原始事件
//   2. 业务层接收并处理平台事件
//   3. 添加上下文信息（如当前应用）
//   4. 发布到事件总线供其他模块消费
package monitor

// Monitor 监控器接口
//
// 定义了所有监控器必须实现的基本行为，包括启动、停止和状态检查。
// 所有监控器都应该实现此接口以确保一致的交互方式。
type Monitor interface {
	// Start 启动监控
	// Returns: error - 启动失败时返回错误，已启动状态下应返回相应错误或幂等处理
	Start() error

	// Stop 停止监控
	// Returns: error - 停止失败时返回错误，未启动状态下应返回相应错误或幂等处理
	Stop() error

	// IsRunning 检查运行状态
	// Returns: bool - true 表示正在运行，false 表示已停止
	IsRunning() bool
}
